package com.aia.premiumadminfee.model;

public class MemberDetailsNoteForPageFooter {

	private String relationship;
	private String medical;
	private String termLife;
	private String underWritingStatus;
	private String movementType;
	public String getRelationship() {
		return relationship;
	}
	public void setRelationship(String relationship) {
		this.relationship = relationship;
	}
	public String getMedical() {
		return medical;
	}
	public void setMedical(String medical) {
		this.medical = medical;
	}
	public String getTermLife() {
		return termLife;
	}
	public void setTermLife(String termLife) {
		this.termLife = termLife;
	}
	public String getUnderWritingStatus() {
		return underWritingStatus;
	}
	public void setUnderWritingStatus(String underWritingStatus) {
		this.underWritingStatus = underWritingStatus;
	}
	public String getMovementType() {
		return movementType;
	}
	public void setMovementType(String movementType) {
		this.movementType = movementType;
	}
	
	
}
