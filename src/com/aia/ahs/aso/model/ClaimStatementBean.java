package com.aia.ahs.aso.model;
public class ClaimStatementBean{
	private String claimNo;
	private String claimOccurance;
	private String claimCompany ;
	public String getClaimNo() {
		return claimNo;
	}
	public String getClaimOccurance() {
		return claimOccurance;
	}
	public String getClaimCompany() {
		return claimCompany;
	}
	public void setClaimNo(String claimNo) {
		this.claimNo = claimNo;
	}
	public void setClaimOccurance(String claimOccurance) {
		this.claimOccurance = claimOccurance;
	}
	public void setClaimCompany(String claimCompany) {
		this.claimCompany = claimCompany;
	}
	
	
}
